#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;

	i = 0;
	while (i < len)
	{
		((unsigned char *)b)[i] = c;
		i++;
	}
	return (b);
}

//c parametresi ile gösterilen bellek bölgesindeki karakteri (unsigned char),
//b parametresindeki bellek bölgesinin ilk len parametre değeri kadar byte'ına kopyalar.
